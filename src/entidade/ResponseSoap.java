package entidade;

public class ResponseSoap {
	
	private int codigoResponse;
	private String mensagemResponse;
	private double valorResponse;
	
	
	public int getCodigoResponse() {
		return codigoResponse;
	}
	public void setCodigoResponse(int codigoResponse) {
		this.codigoResponse = codigoResponse;
	}
	public String getMensagemResponse() {
		return mensagemResponse;
	}
	public void setMensagemResponse(String mensagemResponse) {
		this.mensagemResponse = mensagemResponse;
	}
	public double getValorResponse() {
		return valorResponse;
	}
	public void setValorResponse(double valorResponse) {
		this.valorResponse = valorResponse;
	}
	
	
}
